/// Generate part of Lebedev quadrature with Oh symmetry.
/// 
/// This implementation is more similar to PySCF's implementation of function `SphGenOh`.
#[rustfmt::skip]
#[allow(non_upper_case_globals)]
#[allow(clippy::excessive_precision)]
#[inline]
pub fn lebedev_generate_sph_oh(code: u8, a: f64, b: f64, v: f64, quads: &mut [f64], weights: &mut [f64]) {
    match code {
        0 => {
            // const grid
            const a: f64 = 1.0;
            // 6 points
            quads[..18].copy_from_slice(&[
                 a ,  0.,  0.,
                -a ,  0.,  0.,
                 0.,  a ,  0.,
                 0., -a ,  0.,
                 0.,  0.,  a ,
                 0.,  0., -a ,
            ]);
            weights[..6].fill(v);
        },
        1 => {
            // const grid
            // a = sqrt(1/2)
            const a: f64 = core::f64::consts::FRAC_1_SQRT_2;
            // 12 points
            quads[..36].copy_from_slice(&[
                 0.,  a ,  a ,
                 0., -a ,  a ,
                 0.,  a , -a ,
                 0., -a , -a ,
                 a ,  0.,  a ,
                -a ,  0.,  a ,
                 a ,  0., -a ,
                -a ,  0., -a ,
                 a ,  a ,  0.,
                -a ,  a ,  0.,
                 a , -a ,  0.,
                -a , -a ,  0.,
            ]);
            weights[..12].fill(v);
        },
        2 => {
            // const grid
            // a = sqrt(1/3)
            const a: f64 = 0.577350269189625764509148780501957456_f64;
            // 8 points
            quads[..24].copy_from_slice(&[
                 a ,  a ,  a , 
                -a ,  a ,  a , 
                 a , -a ,  a , 
                -a , -a ,  a , 
                 a ,  a , -a ,
                -a ,  a , -a ,
                 a , -a , -a ,
                -a , -a , -a ,
            ]);
            weights[..8].fill(v);
        },
        3 => {
            let b = (1.0 - 2.0 * a * a).sqrt();
            // 24 points
            quads[..72].copy_from_slice(&[
                 a ,  a ,  b ,
                -a ,  a ,  b ,
                 a , -a ,  b ,
                -a , -a ,  b ,
                 a ,  a , -b ,
                -a ,  a , -b ,
                 a , -a , -b ,
                -a , -a , -b ,
                 a ,  b ,  a ,
                -a ,  b ,  a ,
                 a , -b ,  a ,
                -a , -b ,  a ,
                 a ,  b , -a ,
                -a ,  b , -a ,
                 a , -b , -a ,
                -a , -b , -a ,
                 b ,  a ,  a ,
                -b ,  a ,  a ,
                 b , -a ,  a ,
                -b , -a ,  a ,
                 b ,  a , -a ,
                -b ,  a , -a ,
                 b , -a , -a ,
                -b , -a , -a ,
            ]);
            weights[..24].fill(v);
        },
        4 => {
            let b = (1.0 - a * a).sqrt();
            // 24 points
            quads[..72].copy_from_slice(&[
                 a ,  b ,  0.,
                -a ,  b ,  0.,
                 a , -b ,  0.,
                -a , -b ,  0.,
                 b ,  a ,  0.,
                -b ,  a ,  0.,
                 b , -a ,  0.,
                -b , -a ,  0.,
                 a ,  0.,  b ,
                -a ,  0.,  b ,
                 a ,  0., -b ,
                -a ,  0., -b ,
                 b ,  0.,  a ,
                -b ,  0.,  a ,
                 b ,  0., -a ,
                -b ,  0., -a ,
                 0.,  a ,  b ,
                 0., -a ,  b ,
                 0.,  a , -b ,
                 0., -a , -b ,
                 0.,  b ,  a ,
                 0., -b ,  a ,
                 0.,  b , -a ,
                 0., -b , -a ,
            ]);
            weights[..24].fill(v);
        },
        5 => {
            let c = (1.0 - a * a - b * b).sqrt();
            // 48 points
            quads[..144].copy_from_slice(&[
                 a ,  b ,  c ,
                -a ,  b ,  c ,
                 a , -b ,  c ,
                -a , -b ,  c ,
                 a ,  b , -c ,
                -a ,  b , -c ,
                 a , -b , -c ,
                -a , -b , -c ,
                 a ,  c ,  b ,
                -a ,  c ,  b ,
                 a , -c ,  b ,
                -a , -c ,  b ,
                 a ,  c , -b ,
                -a ,  c , -b ,
                 a , -c , -b ,
                -a , -c , -b ,
                 b ,  a ,  c ,
                -b ,  a ,  c ,
                 b , -a ,  c ,
                -b , -a ,  c ,
                 b ,  a , -c ,
                -b ,  a , -c ,
                 b , -a , -c ,
                -b , -a , -c ,
                 b ,  c ,  a ,
                -b ,  c ,  a ,
                 b , -c ,  a ,
                -b , -c ,  a ,
                 b ,  c , -a ,
                -b ,  c , -a ,
                 b , -c , -a ,
                -b , -c , -a ,
                 c ,  a ,  b ,
                -c ,  a ,  b ,
                 c , -a ,  b ,
                -c , -a ,  b ,
                 c ,  a , -b ,
                -c ,  a , -b ,
                 c , -a , -b ,
                -c , -a , -b ,
                 c ,  b ,  a ,
                -c ,  b ,  a ,
                 c , -b ,  a ,
                -c , -b ,  a ,
                 c ,  b , -a ,
                -c ,  b , -a ,
                 c , -b , -a ,
                -c , -b , -a ,
            ]);
            weights[..48].fill(v);
        }
        _ => unreachable!()
    }
}

